    static func createLevel(_ path: [(Int, Int)], nodeIndices: [Int]) -> LevelDefinition {
        let gridPath = path.map { GridPoint(row: $0.0, col: $0.1) }
        var numberedCells: [Int: GridPoint] = [:]

        numberedCells[1] = gridPath[0]
        numberedCells[nodeIndices.count + 2] = gridPath[35]

        for (nodeNumber, pathIndex) in nodeIndices.enumerated() {
            numberedCells[nodeNumber + 2] = gridPath[pathIndex]
        }

        return LevelDefinition(
            size: 6,
            numberedCells: numberedCells,
            maxNumber: nodeIndices.count + 2,
            solutionPath: gridPath
        )
    }

